    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int count = tblVrndorRTN.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();
        boolean check = false;
        int raw = 0;
        int tbRaw = 0;
        while (count > raw) {
            if (Integer.parseInt(dtm.getValueAt(raw, 1).toString()) == Integer.parseInt(lblPID.getText())) {
                check = true;
                tbRaw = raw;

            }
            raw++;
        }
        if (check) {
            int currentQty = Integer.parseInt(dtm.getValueAt(tbRaw, 5).toString());
            int newQty = currentQty + Integer.parseInt(txtQTY.getText());
            dtm.setValueAt(newQty, tbRaw, 5);
            dtm.setValueAt(newQty * Float.parseFloat(lblPrice.getText()), tbRaw, 6);
        } else {

            count++;

            Vector v = new Vector();
            v.add(count);
            v.add(lblPID.getText());
            v.add(lblBatch.getText());
            v.add(lblDescription.getText());
            v.add(lblPrice.getText());
            v.add(txtQTY.getText());
            v.add(Integer.parseInt(txtQTY.getText()) * Float.parseFloat(lblPrice.getText()));
            dtm.addRow(v);
        }
        lblPID.setText("");
        lblDescription.setText("");
        txtQTY.setText("");
        lblBatch.setText("");
        lblPrice.setText("");

        float total = this.calNet(tblVrndorRTN, 6);
        txtNet.setText(total + "");
        setDiscTotal(total, Float.parseFloat(txtDiscuntPresentage.getText()));
    }//GEN-LAST:event_btnAddActionPerformed

