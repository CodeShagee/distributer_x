    private void loadProductTable(int invoId) {
        InvoiceRegistry Productsobj = new InvoiceRegistry();
        ArrayList<InvoiceRegistry> ProductsList = Productsobj.loadInvoiceRegistrybyInvono(invoId);
        if (ProductsList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No Product Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblProductselect.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (ProductsList.size() > count) {
                Vector v = new Vector();
                InvoiceRegistry Productss = new InvoiceRegistry();
                Productss = ProductsList.get(count);

                v.add(Productss.getPID());
                v.add(Productss.getBatch());
                v.add(Productss.getDescription());
                v.add(Productss.getSPrice());

                dtm.addRow(v);
                count++;

            }
        }

    }

    private float calNet(JTable tbl, int col) {
        float total = 0;
        DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
        for (int i = 0; tbl.getRowCount() > i; i++) {
            total += Float.parseFloat(dtm.getValueAt(i, col).toString());
        }
        return total;
    }

    private void setDiscTotal(float net, float rate) {
        float disc = (net / 100) * rate;
        float total = net - disc;
        txtDiscountValue.setText(disc + "");
        txtTotal.setText(total + "");
    }

    private boolean addSalesRtn(int id, int invoNo, String Customer, String date, float Net, float Discount, float total) {
        boolean status;
        SalesRtn obj = new SalesRtn();
        obj.setRtnNoteNo(id);
        obj.setInvoiceNo(invoNo);
        obj.setCustomer(Customer);
        obj.setDate(date);
        obj.setNet(Net);
        obj.setDiscount(Discount);
        obj.setTotal(total);

        status = obj.addSalesRtn(obj);
        if (status) {
            status = addInvoicesregistryBulk(obj);
        }

        return status;
    }

    private boolean addInvoicesregistryBulk(SalesRtn obj) {
        ArrayList<SalesRtnRegistry> list = new ArrayList<SalesRtnRegistry>();
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();

        for (int raw_id = 0; tblVrndorRTN.getRowCount() > raw_id; raw_id++) {
            SalesRtnRegistry regObj = new SalesRtnRegistry();
            regObj.setRtnNoteNo(obj.getRtnNoteNo());
            regObj.setPID(Integer.parseInt(dtm.getValueAt(raw_id, 1).toString()));
            regObj.setBatch(dtm.getValueAt(raw_id, 2).toString());
            regObj.setDescription(dtm.getValueAt(raw_id, 3).toString());
            regObj.setSPrice(Float.parseFloat(dtm.getValueAt(raw_id, 4).toString()));
            regObj.setQty(Integer.parseInt(dtm.getValueAt(raw_id, 5).toString()));
            regObj.setAmount(Float.parseFloat(dtm.getValueAt(raw_id, 6).toString()));

            list.add(regObj);
        }
        SalesRtnRegistry PRobj = new SalesRtnRegistry();
        boolean stat = PRobj.addSalesRtnRegistryBulk(list);
        if (stat) {
            stat = updateStockQty(list);
        }
        return stat;
    }

    private void clearAll() {
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();
        dtm.setRowCount(0);

        setID();
    }

    public boolean updateStockQty(ArrayList<SalesRtnRegistry> list) {
        Stocks obj = new Stocks();
        boolean stat = false;
        for (int count = 0; list.size() > count; count++) {
            //this is SalesRtn so the type is 1
            SalesRtnRegistry Invoobj = list.get(count);
            stat = obj.updateQty(Invoobj.getPID(), Invoobj.getBatch(), Invoobj.getQty(), 1);
        }
        return stat;
    }
    private void printReport() {
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String dt = df.format(dcRTNDate.getDate());
        try {
            JRTableModelDataSource ds = new JRTableModelDataSource(tblVrndorRTN.getModel());
            String rs = "src\\RPT\\SalesRTN.jrxml";
            Map<String, Object> param = new HashMap<String, Object>();
            param.put("InNo", txtRTNNo.getText());
            param.put("C_name", lblCustomer.getText());
            param.put("pono", txtInvoNo.getText());
            param.put("In_date", dt);
            param.put("Net", txtNet.getText());
            param.put("Total", txtTotal.getText());
            param.put("DiscRate", txtDiscuntPresentage.getText());
            param.put("DiscAmount", txtDiscountValue.getText());

            JasperReport jr = JasperCompileManager.compileReport(rs);
            JasperPrint jp = JasperFillManager.fillReport(jr, param, ds);
            JasperViewer.viewReport(jp, false);

        } catch (JRException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
