    private void loadTable() {
        Customer customerobj = new Customer();
        ArrayList<Customer> customerList = customerobj.loadCustomer();
        if (customerList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No Customer Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (customerList.size() > count) {
                Vector v = new Vector();
                Customer customer = new Customer();
                customer = customerList.get(count);
                v.add(count + 1);
                v.add(customer.getC_id());
                v.add(customer.getC_name());
                v.add(customer.getC_address());
                v.add(customer.getC_contactP());
                v.add(customer.getC_phone());

                dtm.addRow(v);
                count++;

            }
        }

    }

