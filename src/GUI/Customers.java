    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (btnSave.getText() == "Save") {
            boolean stat = this.addCustomer(Integer.parseInt(txtCID.getText()), txtCustName.getText(), txtaAddress.getText(), txtContactPreson.getText(), txtTelephone.getText());
            if (stat) {
                JOptionPane.showMessageDialog(rootPane, "Customer Added Successfully", "Oparation Complete", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error in Customer Adding", "Oparation Incomplete", JOptionPane.ERROR);
            }
            this.loadTable();
            this.clearAll();
        } else {
            boolean stat = this.updateCustomer(Integer.parseInt(txtCID.getText()), txtCustName.getText(), txtaAddress.getText(), txtContactPreson.getText(), txtTelephone.getText());
            if (stat) {
                JOptionPane.showMessageDialog(rootPane, "Customer Added Successfully", "Oparation Complete", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error in Customer Adding", "Oparation Incomplete", JOptionPane.ERROR);
            }
            this.loadTable();
            this.clearAll();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

