private void setUserAuth(UserLevel lvlObj) {

        if (lvlObj.getProduct() == 0) {
            m_itmNewProduct.setVisible(false);
            m_itmViewProducts.setVisible(false);
        }
        if (lvlObj.getBatch() == 0) {
            mnuBatch.setVisible(false);
        }
        if (lvlObj.getCategory() == 0) {
            mnuPcategory.setVisible(false);
        }
        if (lvlObj.getStock() == 0) {
            m_itmStock.setVisible(false);
        }
        if (lvlObj.getCustomer() == 0) {
            mnuCustomer.setVisible(false);
        }
        if (lvlObj.getVendor() == 0) {
            mnuVendor.setVisible(false);
        }
        if (lvlObj.getInvoice() == 0) {
            mnuInvoice.setVisible(false);
        }
        if (lvlObj.getPo() == 0) {
            mnuPotoVendor.setVisible(false);
        }
        if (lvlObj.getSo() == 0) {
            mnuFromCustmr.setVisible(false);
        }
        if (lvlObj.getPurchaseRtn() == 0) {
            mnuRTNV.setVisible(false);
        }
        if (lvlObj.getSaleRtn() == 0) {
            mnuRTNC.setVisible(false);
        }
        if (lvlObj.getGRN() == 0) {
            mnuGRN.setVisible(false);
        }
        if (lvlObj.getUser() == 0) {
            m_itmNewUser.setVisible(false);
        }
        if (lvlObj.getUsarLvl() == 0) {
            m_itmAuthUsers.setVisible(false);
        }
        if (lvlObj.getReports() == 0) {
            mnuReports.setVisible(false);
        }
    }
