private void loadTable() {
        Grn POobj = new Grn();
        ArrayList<Grn> POList = POobj.loadGrn();
        if (POList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No PO Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblGRNView.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (POList.size() > count) {
                Vector v = new Vector();
                Grn po = new Grn();
                po = POList.get(count);
                v.add(count + 1);
                v.add(po.getGRNno());
                v.add(po.getPoNo());
                v.add(po.getGrnDate());
                v.add(po.getBatch());
                v.add(po.getVendorName());
                v.add(po.getTotal());

                dtm.addRow(v);
                count++;

            }
        }

    }

    private void findSalesOrderByFilter(String vendor, String fDate, String tDate) {
        Grn POobj = new Grn();
        ArrayList<Grn> POList = POobj.searchGRNByFilter(vendor, fDate, tDate);
        if (POList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No Sales Order Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblGRNView.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (POList.size() > count) {
                Vector v = new Vector();
                Grn po = new Grn();
                po = POList.get(count);
                v.add(count + 1);
                v.add(po.getGRNno());
                v.add(po.getPoNo());
                v.add(po.getGrnDate());
                v.add(po.getBatch());
                v.add(po.getVendorName());
                v.add(po.getTotal());

                dtm.addRow(v);
                count++;

            }
        }

    }
