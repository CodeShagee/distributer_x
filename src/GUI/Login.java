private boolean checkPW(char[] userPW, char[] dbPW) {
        boolean result = false;
        if (userPW.length == dbPW.length) {
            result = true;
        }
        if (result) {
            for (int i = 0; userPW.length > i; i++) {
                if (userPW[i] == dbPW[i]) {
                    result = true;
                } else {
                    result = false;
                    break;
                }
            }
        }
        return result;
    }
