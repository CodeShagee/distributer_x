    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if ((txtUsername.getText().isEmpty()) | (ptxtPassword.getText().isEmpty())) {
            JOptionPane.showMessageDialog(rootPane, "Username and Password can not be empty!", "Login Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String userName = txtUsername.getText().trim();
            if (new User().isExsist(userName)) {
                User loadObj = new User().getUserByUserName(userName);
                if (loadObj != null) {
                    char[] pwarydb = loadObj.getPassword().toCharArray();
                    char[] pwaryusr = ptxtPassword.getPassword();
                    if (checkPW(pwaryusr, pwarydb)) {
                        UserLevel lvlObj = new UserLevel().getUserLevel(loadObj.getLevel());
                        DistributerXHome obj = new DistributerXHome(lvlObj, userName);
                        obj.setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Invalid Username and Password !", "Login Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Invalid Username and Password !", "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

