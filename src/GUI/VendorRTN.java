 private void loadProductTable(int invoId) {
        GrnRegistry Productsobj = new GrnRegistry();
        ArrayList<GrnRegistry> ProductsList = Productsobj.loadGrnRegistrybyGRNno(invoId);
        if (ProductsList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No Product Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblProductselect.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (ProductsList.size() > count) {
                Vector v = new Vector();
                GrnRegistry Productss = new GrnRegistry();
                Productss = ProductsList.get(count);

                v.add(Productss.getPID());
                v.add(Productss.getBatch());
                v.add(Productss.getPDescription());
                v.add(Productss.getSPrice());

                dtm.addRow(v);
                count++;

            }
        }

    }
  private float calNet(JTable tbl, int col) {
        float total = 0;
        DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
        for (int i = 0; tbl.getRowCount() > i; i++) {
            total += Float.parseFloat(dtm.getValueAt(i, col).toString());
        }
        return total;
    }
     
      private boolean addPurchaseRtn(int id, int GrnNo, String Vendor, String date, float total) {
        boolean status;
        PurchaseRtn obj = new PurchaseRtn();
        obj.setRtnNoteNo(id);
        obj.setGrnNo(GrnNo);
        obj.setVendor(Vendor);
        obj.setDate(date);
        obj.setTotal(total);
        

        status = obj.addPurchaseRtn(obj);
        if (status) {
            status = addInvoicesregistryBulk(obj);
        }
        
        

        return status;
    }

    private boolean addInvoicesregistryBulk(PurchaseRtn obj) {
        ArrayList<PurchaseRtnRegistry> list = new ArrayList<PurchaseRtnRegistry>();
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();

        for (int raw_id = 0; tblVrndorRTN.getRowCount() > raw_id; raw_id++) {
            PurchaseRtnRegistry regObj = new PurchaseRtnRegistry();
            regObj.setRtnNoteNo(obj.getRtnNoteNo());
            regObj.setPID(Integer.parseInt(dtm.getValueAt(raw_id, 1).toString()));
            regObj.setBatch(dtm.getValueAt(raw_id, 2).toString());
            regObj.setPDescription(dtm.getValueAt(raw_id, 3).toString());
            regObj.setBPrice(Float.parseFloat(dtm.getValueAt(raw_id, 4).toString()));
            regObj.setQty(Integer.parseInt(dtm.getValueAt(raw_id, 5).toString()));
            regObj.setAmount(Float.parseFloat(dtm.getValueAt(raw_id, 6).toString()));

            list.add(regObj);
        }
        PurchaseRtnRegistry PRobj = new PurchaseRtnRegistry();
        boolean stat = PRobj.addPurchaseRtnRegistryBulk(list);
        if(stat)
        {
            stat=updateStockQty(list);
        }
        return stat;
    }

