    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtQTY.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, " Qty Cannot be Empty!", "Data Missing", JOptionPane.ERROR_MESSAGE);
        } else {
        int count = tblVrndorRTN.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();
        boolean check = false;
        int raw = 0;
        int tbRaw = 0;
        while (count > raw) {
            if (Integer.parseInt(dtm.getValueAt(raw, 1).toString()) == Integer.parseInt(lblPID.getText())) {
                check = true;
                tbRaw = raw;

            }
            raw++;
        }
        if (check) {
            int currentQty = Integer.parseInt(dtm.getValueAt(tbRaw, 5).toString());
            int newQty = currentQty + Integer.parseInt(txtQTY.getText());
            dtm.setValueAt(newQty, tbRaw, 5);
            dtm.setValueAt(newQty * Float.parseFloat(lblPrice.getText()), tbRaw, 6);
        } else {

            count++;

            Vector v = new Vector();
            v.add(count);
            v.add(lblPID.getText());
            v.add(lblBatch.getText());
            v.add(lblDescription.getText());
            v.add(lblPrice.getText());
            v.add(txtQTY.getText());
            v.add(Integer.parseInt(txtQTY.getText()) * Float.parseFloat(lblPrice.getText()));
            dtm.addRow(v);
        }
        lblPID.setText("");
        lblDescription.setText("");
        txtQTY.setText("");
        lblBatch.setText("");
        lblPrice.setText("");

        float total = this.calNet(tblVrndorRTN, 6);
        txtTotal.setText(total + "");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRmvItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRmvItemActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblVrndorRTN.getModel();
        if (dtm.getRowCount() > 0) {
            try {
        dtm.removeRow(tblVrndorRTN.getSelectedRow());
        float net = this.calNet(tblVrndorRTN, 6);
        txtTotal.setText( net+ "");
        } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Please Select a Raw !", "Oops", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No Data Available !", "Oops", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRmvItemActionPerformed

