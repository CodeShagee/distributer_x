private void loadTable() {
        PurchaseRtn POobj = new PurchaseRtn();
        ArrayList<PurchaseRtn> POList = POobj.loadPurchaseRtn();
        if (POList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No PO Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblRTNView.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (POList.size() > count) {
                Vector v = new Vector();
                PurchaseRtn po = new PurchaseRtn();
                po = POList.get(count);
                v.add(count + 1);
                v.add(po.getRtnNoteNo());
                v.add(po.getGrnNo());
                v.add(po.getDate());
                v.add(po.getVendor());
                v.add(po.getTotal());
                
                

                dtm.addRow(v);
                count++;

            }
        }

    }
private void findSalesOrderByFilter(String vendor,String fDate,String tDate) {
        PurchaseRtn POobj = new PurchaseRtn();
        ArrayList<PurchaseRtn> POList = POobj.searchGRNByFilter(vendor, fDate, tDate);
        if (POList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sorry ! No Sales Order Found.");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) tblRTNView.getModel();
            dtm.setRowCount(0);
            int count = 0;
            while (POList.size() > count) {
                Vector v = new Vector();
                PurchaseRtn po = new PurchaseRtn();
                po = POList.get(count);
                v.add(count + 1);
                v.add(po.getRtnNoteNo());
                v.add(po.getGrnNo());
                v.add(po.getDate());
                v.add(po.getVendor());
                v.add(po.getTotal());
                
                

                dtm.addRow(v);
                count++;

            }
        }

    }
private void printReport() {
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String fdt=null;
        String tdt=null;
        if(dcFrom.getDate()!=null){
         fdt = df.format(dcFrom.getDate());
        }
        if(dcTo.getDate()!=null){
         tdt = df.format(dcTo.getDate());
        }
        try {
            JRTableModelDataSource ds = new JRTableModelDataSource(tblRTNView.getModel());
            String rs = "src\\RPT\\PurchaseRtnRpt.jrxml";
            Map<String, Object> param = new HashMap<String, Object>();
            param.put("fDate", fdt);
            param.put("tDate", tdt);
            
            

            JasperReport jr = JasperCompileManager.compileReport(rs);
            JasperPrint jp = JasperFillManager.fillReport(jr, param, ds);
            JasperViewer.viewReport(jp, false);

        } catch (JRException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
